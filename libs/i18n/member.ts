export const resolveLocale = (locale?: string): LocaleKey =>
  (SUPPORTED_LOCALES.includes(locale as LocaleKey)
    ? locale
    : "en") as LocaleKey;

export const getMemberText = (
  locale: LocaleKey,
  key: string,
  fallback?: string,
): string => MEMBER_TRANSLATIONS[locale]?.[key] ?? fallback ?? key;
